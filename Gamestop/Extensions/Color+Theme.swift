import Foundation
import SwiftUI

extension Color {
    
    static let theme = ColorTheme()
}

struct ColorTheme {
    
    let background = Color("BackgroundColor")
    let primaryText = Color("PrimaryTextColor")
    let midnight = Color("MidnightColor")
    let gray = Color("PurpleColor")
    let pink = Color("PinkColor")
}
